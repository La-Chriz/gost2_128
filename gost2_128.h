static void hash_init(h_ctx_t *h_ctx)
{
    
   h_ctx->x1 = h_ctx->x2 = 0;
    for (int i = 0; i < n1; i++)
        h_ctx->h2[i] = h_ctx->h1[i] = 0;
}

static void hash_update(h_ctx_t *h_ctx, uint8_t *data, size_t len)
{
  int b1,b2,b3,b4,b5;

  b4=0;
  while (len) {
    for (; len && h_ctx->x2 < n1; len--, h_ctx->x2++)
    {
      b5 = data[b4++];
      h_ctx->h1[h_ctx->x2 + n1] = b5;
      h_ctx->h1[h_ctx->x2 + (n1*2)] = b5 ^ h_ctx->h1[h_ctx->x2];
      h_ctx->x1 = h_ctx->h2[h_ctx->x2] ^= s4[b5 ^ h_ctx->x1];
    }

    if (h_ctx->x2 == n1)
    {
      b2 = 0;
      h_ctx->x2 = 0;
            
      for (b3 = 0; b3 < (n1+2); b3++)
      {
        for (b1 = 0; b1 < (n1*3); b1++)
          b2 = h_ctx->h1[b1] ^= s4[b2];
        b2 = (b2 + b3) % 256;
      }
    }
  }
}

static void hash_final(h_ctx_t *h_ctx, uint8_t hash[n1])
{
    
    uint8_t h3[n1];
    int i, n4;
    
    n4 = n1 - h_ctx->x2;
    for (i = 0; i < n4; i++) h3[i] = n4;
    hash_update(h_ctx, h3, n4);
    hash_update(h_ctx, h_ctx->h2, sizeof(h_ctx->h2));
    for (i = 0; i < n1; i++) hash[i] = h_ctx->h1[i];
}

