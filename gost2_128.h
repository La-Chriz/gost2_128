gost2_128_error_t gost2_128_init(gost2_128_context_t *ctx, uint8_t *key, size_t len, int rounds)
{
  if(!ctx || !key)
    return GOST2_128_PARAMETER_ERROR;

  if(len != 32)
    return GOST2_128_KEY_SIZE_ERROR;

  (void) rounds;

  h_ctx_t h_ctx;
  uint8_t hash[n1];

  hash_init(&h_ctx);
  hash_update(&h_ctx, key, len);
  hash_final(&h_ctx, hash);

  int k=0;
  for (int i=0;i<64;i++)
  {
    for (int z=0;z<8;z++)
      ctx->k[i]=(ctx->k[i]<<8)+(hash[k++]&0xff);
  }

  return GOST2_128_NO_ERROR;
}

