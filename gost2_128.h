static inline uint64_t gost2_128_load64(const uint8_t p[8])
{
  return ((uint64_t)p[0] << 56) |
         ((uint64_t)p[1] << 48) |
         ((uint64_t)p[2] << 40) |
         ((uint64_t)p[3] << 32) |
         ((uint64_t)p[4] << 24) |
         ((uint64_t)p[5] << 16) |
         ((uint64_t)p[6] <<  8) |
         ((uint64_t)p[7]);
}

static inline void gost2_128_store64(uint8_t p[8], uint64_t x)
{
  p[0] = (uint8_t)((x >> 56) & 0xFF);
  p[1] = (uint8_t)((x >> 48) & 0xFF);
  p[2] = (uint8_t)((x >> 40) & 0xFF);
  p[3] = (uint8_t)((x >> 32) & 0xFF);
  p[4] = (uint8_t)((x >> 24) & 0xFF);
  p[5] = (uint8_t)((x >> 16) & 0xFF);
  p[6] = (uint8_t)((x >>  8) & 0xFF);
  p[7] = (uint8_t)(x & 0xFF);
}

