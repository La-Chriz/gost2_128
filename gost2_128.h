static uint64_t f(uint64_t x)
{
	/* Do substitutions */
	uint64_t y,z;

	y = x >> 32;
	z = x & 0xffffffff;

	y = gost2_128_k[3][y>>24 & 0xff] << 24 |
     gost2_128_k[2][y>>16 & 0xff] << 16 |
	    gost2_128_k[1][y>> 8 & 0xff] <<  8 |
     gost2_128_k[0][y & 0xff];
	    
	z = gost2_128_k[7][z>>24 & 0xff] << 24 |
     gost2_128_k[6][z>>16 & 0xff] << 16 |
	    gost2_128_k[5][z>> 8 & 0xff] <<  8 |
     gost2_128_k[4][z & 0xff];

    x = y << 32;
    x = x | (z & 0xffffffff);
   
	/* Rotate left 11 bits */
	return (x<<11) | (x>>53);
	
}

gost2_128_error_t gost2_128_encrypt(const gost2_128_context_t *ctx, uint8_t const in[16], uint8_t out[16])
{
  if(!ctx || !in || !out)
    return GOST2_128_PARAMETER_ERROR;

	register uint64_t ngost1, ngost2; 

	ngost1 = gost2_128_load64(&in[0]);
	ngost2 = gost2_128_load64(&in[8]);
	
	int k=0;
	
	for (int i=0;i<32;i++)
	{
	    ngost2 ^= f(ngost1+ctx->k[k++]);
	    ngost1 ^= f(ngost2+ctx->k[k++]);
	    
	}	
	
	/* There is no swap after the last round */
	gost2_128_store64(&out[0], ngost2);
	gost2_128_store64(&out[8], ngost1);

  return GOST2_128_NO_ERROR;
}
	
gost2_128_error_t gost2_128_decrypt(const gost2_128_context_t *ctx, uint8_t const in[16], uint8_t out[16])
{
  if(!ctx || !in || !out)
    return GOST2_128_PARAMETER_ERROR;

	register uint64_t ngost1, ngost2; 

	ngost1 = gost2_128_load64(&in[0]);
	ngost2 = gost2_128_load64(&in[8]);
	
	int k=63;
	
	for (int i=0;i<32;i++)
	{
	    ngost2 ^= f(ngost1+ctx->k[k--]);
	    ngost1 ^= f(ngost2+ctx->k[k--]);
	}	

	gost2_128_store64(&out[0], ngost2);
	gost2_128_store64(&out[8], ngost1);

  return GOST2_128_NO_ERROR;
}

